    <div class="swunderline">How To Apply For WIC</div>
    <br/><br/>
    <div class="swmidtitle">Give WIC a Call!</div>


    <div class="swcontent">To set up an appointment to apply for WIC, call the WIC office in your county. You will need
        to bring each person who is applying for benefits to this first appointment, including infants and all
        children&#160;up to age five. &#160;In preparation for your appointment, please bring the following items:
        <br/><br/>

        <table width="85%" align="center" cellspacing="0" cellpadding="0">
            <tbody>

                <tr>
                    <td width="25%" valign="top">
                        <h4><strong>Proof of Household Income</strong></h4>
                    </td>
                    <td>
                        <h4>(Bring at least ONE)</h4>
                        <ul>
                            <li>Recent pay stubs, or benefit letter: SSI, Social Security, Unemployment<strong></strong>
                            </li>
                            <li>Medical Assistance card<strong></strong></li>
                            <li>Independence Card with register receipt (30 days)<strong></strong></li>
                            <li>Tax Return<strong></strong></li>
                        </ul>
                    </td>
                </tr>

                <tr>
                    <td valign="top" style="text-align: left;">
                        <h4><strong>Person(s) being certified and proof of identity</strong></h4>
                    </td>
                    <td>
                        <h4>(Bring ONE below, per person)</h4>
                        <ul>
                            <li>Birth Certificate</li>
                            <li>Driver&#39;s License or MVA identification</li>
                            <li>Social Security Card</li>
                            <li>Picture Identification (Government, School, Employment)</li>
                        </ul>
                    </td>
                </tr>

                <tr>
                    <td valign="top" style="text-align: left;">
                        <h4><strong>Proof of your address</strong></h4>
                    </td>
                    <td>
                        <h4>(Bring ONE)</h4>
                        <ul>
                            <li>Driver&#39;s License or MVA Identification</li>
                            <li>Official Bill or Statement</li>
                            <li>Copy of Lease</li>
                        </ul>
                    </td>
                </tr>
                <tr>
                    <td valign="top" style="text-align: left;">
                        <h4><strong>If applicable</strong></h4>
                    </td>
                    <td>
                        <ul>
                            <li>Proof of pregnancy</li>
                            <li>Immunization record (infant or child)</li>
                            <li>WIC referral</li>
                        </ul>
                    </td>
                </tr>
            </tbody>
        </table>



